public class SelfCheckSeven {
    public static void main(String[] args) {
        printHouse("jDaddy", 69);
    }
    
    public static void printHouse(String msg, int reps) {
        for(int i=0; i<reps; i++) {
            System.out.print(msg + " ");
        }
    }
}
